
// Unary opetratörler: tek değer kullanan operatörlerdir örnek olarak Değil operatörü (!x) x'in değili
// binary operatörler: iki değer kullanırlarlar örneğin (x + y) toplama operatörü
// ternary operatörler: üç değerle kullanılar operatörler örneği koşul (x ? y : z) swifte özeldir

// atama operatörü =

let x = 1
var y = 4

y = x

// aritmatik operatörler + - / * %

print(1 + 2) // toplama

print(1 - 2) // çıkartma

print(5 / 2) // bölme

print(1 * 2) // çarpma

print(15 % 3) // mod

print("ahmet" + " hakkı")

// bileşik atama operatörü -= += /= *=

var değer = 2
değer += 2

// karılaştırma operatörleri == != >= <= < >

print(1 == 2) // eşit mi

print(1 != 2) // eşit değil mi

print(1 <= 2) // küçük yada eşit mi

print(1 >= 2) // büyük yada eşit mi

print(1 < 2)  // küçük mü

print(1 > 2)  // büyük mü

print("x" == "x")

//koşul operatörü (x ? y : z) yada (True or false ? is true : is false)

"x" == "x" ? print("eşit") : print("eşit değil") // output eşit
"x" == "y" ? print("eşit") : print("eşit değil") // output eşit değil

